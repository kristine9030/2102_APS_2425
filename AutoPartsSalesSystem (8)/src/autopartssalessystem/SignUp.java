    private void SignUpBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignUpBtn1ActionPerformed
        // TODO add your handling code here:
        String Name, Email, Password, Address, PhoneNumber;
        String SUrl = "jdbc:mysql://localhost:3306/autopartssales_db";
        String SUser = "root";
        String SPass = "your_password";
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
            Statement st = con.createStatement();

            // Validate input fields
            if (uname.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(new JFrame(), "Username is required", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (emailAddress.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(new JFrame(), "Email Address is required", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (pass.getPassword().length == 0) {
                JOptionPane.showMessageDialog(new JFrame(), "Password is required", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (AddressTextField.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(new JFrame(), "Address is required", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (PhoneNumberTextField.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(new JFrame(), "Phone Number is required", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Collect input values
            Name = uname.getText().trim();
            Email = emailAddress.getText().trim();
            Password = new String(pass.getPassword()).trim();
            Address = AddressTextField.getText().trim();
            PhoneNumber = PhoneNumberTextField.getText().trim();

            // Insert query
            String query = "INSERT INTO customer (Name, Email, Password, Address, PhoneNumber) VALUES ('"
                    + Name + "', '"
                    + Email + "', '"
                    + Password + "', '"
                    + Address + "', '"
                    + PhoneNumber + "')";

            st.executeUpdate(query);

            // Clear form fields
            uname.setText("");
            emailAddress.setText("");
            pass.setText("");
            AddressTextField.setText("");
            PhoneNumberTextField.setText("");

            // Show success message
            JOptionPane.showMessageDialog(null, "New Account has been created successfully!");

            // Redirect to Login
            Login loginFrame = new Login();
            loginFrame.setVisible(true);
            this.dispose();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(new JFrame(), "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_SignUpBtn1ActionPerformed

