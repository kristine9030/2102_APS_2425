    private void LoginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginBtnActionPerformed
        // System.out.println("Login");
        String email1 = email.getText();
        String password1 = new String(password.getPassword());
        String SUrl = "jdbc:mysql://localhost:3306/java_user_database";
        String SUser = "root";
        String SPass = "";

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(SUrl, SUser, SPass);
            Statement st = con.createStatement();

            // Query to check if the entered email and password match any records in the user table
            String query = "SELECT * FROM user1 WHERE email = '" + email1 + "' AND password = '" + password1 + "'";
            ResultSet rs = st.executeQuery(query);

            if (rs.next()) {
                // Get the user role
                String role = rs.getString("role"); // assuming the table has a column named 'role'

                if ("admin".equalsIgnoreCase(role)) {
                    JOptionPane.showMessageDialog(new JFrame(), "Admin Login Successful!");

                    // Redirect to AdminDashboard class in the Admin package
                    AdminDashboard adminDashboard = new AdminDashboard();
                    adminDashboard.setVisible(true);
                    this.dispose();

                } else if ("customer".equalsIgnoreCase(role)) {
                    JOptionPane.showMessageDialog(new JFrame(), "Customer Login Successful!");

                    // Redirect to CustomerDashboard and pass the email as username
                    CustomerDashboard customerDashboard = new CustomerDashboard();
                 
                    customerDashboard.setVisible(true);
                    this.dispose();

                } else {
                    JOptionPane.showMessageDialog(new JFrame(), "Unknown role. Access denied.", "Access Denied", JOptionPane.WARNING_MESSAGE);
                }

            } else {
                // No match found, show error message
                JOptionPane.showMessageDialog(new JFrame(), "Incorrect email or password. Please try again.", "Login Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            System.out.println("Error!" + e.getMessage());
            JOptionPane.showMessageDialog(new JFrame(), "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_LoginBtnActionPerformed

